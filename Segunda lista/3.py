'''  Criar um programa que receba a base e a altura do triângulo, calcule e imprima a área do mesmo. A área do triângulo é dada pela seguinte fórmula: Área = ( Base * Altura ) / 2.'''


altura = float(input('Digite a altura do triângulo: '))
base = float(input('Digite a base do triângulo: '))
area = ((base * altura) / 2)
print("A área desse triângulo é", area)
